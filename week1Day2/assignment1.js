function numCheck(myNum){
    if (myNum>0){
        console.log(`The number ${myNum} is Positive`)
    }else if(myNum<0){
        console.log(`The number ${myNum} is Negative`)
    }else{
        console.log(`The number ${myNum} is Zero`)
    }
}
numCheck(1.1)